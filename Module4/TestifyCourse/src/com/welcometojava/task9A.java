package com.welcometojava;

public class task9A {
    public static void main(String[] args) {
        for (int num = 0; num <= 10; num++) {
            if (num % 2 == 0) {
                System.out.println(num + " " + "is an even number");
            } else {
                System.out.println(num + " " + "is an odd number");
            }
        }
    }

}











