package com.welcometojava;

public class BAsicArithmetic {
    public static void main(String[] args) {
        //basic arithmetic
        int age1 = 20;
        int age2 = 30;
        System.out.println(age1+age2);

        //incremental and decremental
        System.out.println(age1++);

        //compund assignment operator; +=, /= etc
        age1 -= age2;
        System.out.println(age1);

    }
}
